
public class Player {
	private int id;
	private String name;
	private long x;
	private long y;
	private String verein;
	private int age;
	private String mw;
	private String datum_mw;
	private String marker;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getX() {
		return x;
	}
	public void setX(long x) {
		this.x = x;
	}
	public long getY() {
		return y;
	}
	public void setY(long y) {
		this.y = y;
	}
	public String getVerein() {
		return verein;
	}
	public void setVerein(String verein) {
		this.verein = verein;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getMw() {
		return mw;
	}
	public void setMw(String mw) {
		this.mw = mw;
	}
	public String getDatum_mw() {
		return datum_mw;
	}
	public void setDatum_mw(String datum_mw) {
		this.datum_mw = datum_mw;
	}
	public String getMarker() {
		return marker;
	}
	public void setMarker(String marker) {
		this.marker = marker;
	}
	
	
}

